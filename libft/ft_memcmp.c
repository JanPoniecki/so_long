#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned char const	*ts1;
	unsigned char const	*ts2;
	size_t				i;

	i = 0;
	ts1 = s1;
	ts2 = s2;
	while (i < n)
	{
		if (ts1[i] != ts2[i])
			return (ts1[i] - ts2[i]);
		i++;
	}
	return (0);
}
