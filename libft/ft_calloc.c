#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	size_t	max_val;
	void	*ptr;

	max_val = -1;
	if (nmemb != 0 && max_val / nmemb < size)
		return (NULL);
	ptr = (void *)malloc(nmemb * size);
	if (!ptr)
		return (NULL);
	ft_bzero(ptr, nmemb * size);
	return (ptr);
}
