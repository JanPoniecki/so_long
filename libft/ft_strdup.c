#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*result;
	int		i;

	result = (char *)malloc(ft_strlen(s) * sizeof(char) + 1);
	if (!result)
		return (NULL);
	i = 0;
	while (s[i])
	{
		result[i] = s[i];
		i++;
	}
	result[i] = 0;
	return (result);
}
